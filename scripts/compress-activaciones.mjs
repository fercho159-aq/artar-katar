#!/usr/bin/env node
/**
 * Compress MP3/WAV audio files for the Activaciones Diarias program.
 *
 * Usage:
 *   node scripts/compress-activaciones.mjs <source-dir> [--bitrate 64]
 *
 * Outputs:
 *   - Compressed files written to ./private-audio/ (mono, 64 kbps MP3)
 *   - Filenames normalized to slug form (lowercase, no accents, hyphen-separated)
 *   - Prints SQL INSERT statements for the `activaciones` table
 *
 * Requires: ffmpeg and ffprobe in PATH.
 */
import { spawnSync } from 'node:child_process';
import { readdirSync, statSync, mkdirSync, existsSync } from 'node:fs';
import { join, extname, basename, resolve } from 'node:path';

function slugify(str) {
  return str
    .normalize('NFD')
    .replace(/\p{Diacritic}+/gu, '')
    .toLowerCase()
    .replace(/&/g, ' y ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 80);
}

function titleFromFilename(name) {
  // strip extension, keep readable
  return name.replace(extname(name), '').trim();
}

function probeDuration(filePath) {
  const res = spawnSync('ffprobe', [
    '-v', 'error',
    '-show_entries', 'format=duration',
    '-of', 'default=noprint_wrappers=1:nokey=1',
    filePath,
  ], { encoding: 'utf8' });
  if (res.status !== 0) return null;
  const sec = parseFloat(res.stdout.trim());
  return Number.isFinite(sec) ? Math.round(sec) : null;
}

function compress(inputPath, outputPath, bitrate) {
  const res = spawnSync('ffmpeg', [
    '-y',
    '-i', inputPath,
    '-ac', '1',
    '-b:a', `${bitrate}k`,
    '-codec:a', 'libmp3lame',
    outputPath,
  ], { stdio: 'inherit' });
  return res.status === 0;
}

function escapeSql(str) {
  return String(str).replace(/'/g, "''");
}

function main() {
  const args = process.argv.slice(2);
  if (args.length === 0) {
    console.error('Usage: node scripts/compress-activaciones.mjs <source-dir> [--bitrate 64]');
    process.exit(1);
  }
  const sourceDir = resolve(args[0]);
  const bitrateIdx = args.indexOf('--bitrate');
  const bitrate = bitrateIdx >= 0 ? parseInt(args[bitrateIdx + 1], 10) : 64;

  if (!existsSync(sourceDir) || !statSync(sourceDir).isDirectory()) {
    console.error(`Source dir not found: ${sourceDir}`);
    process.exit(1);
  }

  const destDir = resolve(process.cwd(), 'private-audio');
  if (!existsSync(destDir)) mkdirSync(destDir, { recursive: true });

  const files = readdirSync(sourceDir)
    .filter((f) => ['.mp3', '.wav', '.m4a'].includes(extname(f).toLowerCase()));

  if (files.length === 0) {
    console.error('No audio files found.');
    process.exit(1);
  }

  console.error(`Compressing ${files.length} file(s) at ${bitrate} kbps mono...`);

  const inserts = [];
  let sortOrder = 10;

  for (const file of files) {
    const inputPath = join(sourceDir, file);
    const title = titleFromFilename(file);
    const slug = slugify(title);
    const outputFile = `${slug}.mp3`;
    const outputPath = join(destDir, outputFile);

    console.error(`\n→ ${file}\n  slug: ${slug}`);

    const ok = compress(inputPath, outputPath, bitrate);
    if (!ok) {
      console.error(`  FAILED: ${file}`);
      continue;
    }

    const duration = probeDuration(outputPath);

    inserts.push(
      `INSERT INTO activaciones (slug, title, description, category, duration_seconds, audio_filename, sort_order) ` +
      `VALUES ('${escapeSql(slug)}', '${escapeSql(title)}', NULL, NULL, ${duration ?? 'NULL'}, ` +
      `'${escapeSql(outputFile)}', ${sortOrder}) ON CONFLICT (slug) DO NOTHING;`
    );
    sortOrder += 10;
  }

  console.error(`\nDone. Output dir: ${destDir}`);
  console.error('\nCopy the SQL below and run it against your DATABASE_URL:\n');
  console.log('-- Generated by compress-activaciones.mjs');
  console.log(inserts.join('\n'));
}

main();
